{
    func saveImagePath( imagePath : String)
}

class ModalImageTableViewController: UITableViewController {

    var delegateModal: ModalViewControllerDelegate?
    
    var arrayImagen: [String]?
    
    
    override func viewDidLoad() {
        super.viewDidLoad()
        
        
        
